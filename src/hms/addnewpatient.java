    private void doctorName(){
        try {
            Connection con=connectionprovider.getCon();
            PreparedStatement insert=con.prepareStatement("select *from doctor_details");
            ResultSet rs=(ResultSet) insert.executeQuery();
            while(rs.next()){
                String doctorName=rs.getString("doctor_name_department");
                jComboBox3.addItem(doctorName);
            }
        } catch (SQLException ex) {
            Logger.getLogger(addnewpatient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
     private void patientId(){
       try {
            Connection con=connectionprovider.getCon();
            PreparedStatement insert=con.prepareStatement("select Max(patient_id) from patient_details");
            ResultSet rs=(ResultSet) insert.executeQuery();
            rs.next();
            rs.getString("Max(patient_id)");
            if( rs.getString("Max(patient_id)")==null){
                jTextField1.setText("P0001");
            }
           else
            {
                long patient_id=Long.parseLong(rs.getString("Max(patient_id)").substring(2,rs.getString("Max(patient_id)").length()));
                patient_id++;
                jTextField1.setText("P000"+String.format("%d", patient_id));
                
            }
          
          
        } catch (SQLException ex) {
            Logger.getLogger(addnewdoctor.class.getName()).log(Level.SEVERE, null, ex);
        }
  }

