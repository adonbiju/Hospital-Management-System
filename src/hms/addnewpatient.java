    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // save button function
        String id=jTextField1.getText();
        String name=jTextField2.getText();
        String age=jTextField3.getText();
        String gender=(String) jComboBox1.getSelectedItem();
        String blood=jTextField4.getText();
        String mobile=jTextField5.getText();
        String disease;
        String diseasename;
        String doctor=(String) jComboBox3.getSelectedItem();
        if(jCheckBox1.isSelected())
        {
            disease="Yes";
            diseasename=jTextField6.getText();
                    
        }
        else
        {
             disease="No";
             diseasename="";
        }
        
        try {
            Connection con=connectionprovider.getCon();
            PreparedStatement insert = con.prepareStatement("insert into patient_details(patient_id,name,age,gender,blood,mobile,disease,disease_name,doctor_name_department)values(?,?,?,?,?,?,?,?,?)");
            insert.setString(1, id);
            insert.setString(2, name);
            insert.setString(3, age);
            insert.setString(4, gender);
            insert.setString(5, blood);
            insert.setString(6, mobile);
            insert.setString(7, disease);
            insert.setString(8, diseasename);
            insert.setString(9, doctor);
            insert.executeUpdate();
            JOptionPane.showMessageDialog(null, "Sucessfully registered");
            clear();
            patientId();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Fill the form Correctly");
            Logger.getLogger(addnewpatient.class.getName()).log(Level.SEVERE, null, ex);
        }
           
    }//GEN-LAST:event_jButton1ActionPerformed

