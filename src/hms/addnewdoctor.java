     private void departmentName(){
        try {
            Connection con=connectionprovider.getCon();
            PreparedStatement insert=con.prepareStatement("select *from department");
            com.mysql.jdbc.ResultSet rs=(com.mysql.jdbc.ResultSet) insert.executeQuery();
            while(rs.next()){
                String departmentName=rs.getString("department");
                jComboBox2.addItem(departmentName);
            }
        } catch (SQLException ex) {
            Logger.getLogger(addnewpatient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
  private void doctorId(){
       try {
            Connection con=connectionprovider.getCon();
            PreparedStatement insert=con.prepareStatement("select Max(doctor_id) from doctor_details");
            ResultSet rs=(com.mysql.jdbc.ResultSet) insert.executeQuery();
            rs.next();
            rs.getString("Max(doctor_id)");
            if( rs.getString("Max(doctor_id)")==null){
                jTextField1.setText("D0001");
            }
           else
            {
                long doctor_id=Long.parseLong(rs.getString("Max(doctor_id)").substring(2,rs.getString("Max(doctor_id)").length()));
                doctor_id++;
                jTextField1.setText("D000"+String.format("%d", doctor_id));
                
            }
          
          
        } catch (SQLException ex) {
            Logger.getLogger(addnewdoctor.class.getName()).log(Level.SEVERE, null, ex);
        }
  }
