    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // save button
        String id=jTextField1.getText();
        String name=jTextField2.getText();
        String age=jTextField3.getText();
        String gender=(String) jComboBox1.getSelectedItem();
        String department=(String) jComboBox2.getSelectedItem();
        String mobile=jTextField5.getText();
        String address=jTextArea1.getText();
        String namedepartment=jTextField8.getText();
        String username=jTextField6.getText();
        String password=jPasswordField1.getText();
        
        try {
            Connection con=connectionprovider.getCon();
            PreparedStatement insert=con.prepareStatement("insert into doctor_details(doctor_id,name,age,gender,department,mobile,address,doctor_name_department,username,password)values(?,?,?,?,?,?,?,?,?,?)");
            insert.setString(1, id);
            insert.setString(2, name);
            insert.setString(3, age);
            insert.setString(4, gender);
            insert.setString(5, department);
            insert.setString(6, mobile);
            insert.setString(7, address);
            insert.setString(8, namedepartment);
            insert.setString(9, username);
            insert.setString(10, password);
            insert.executeUpdate();
            JOptionPane.showMessageDialog(null, "Sucessfully registered");
            clear();
            doctorId();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Fill the form Correctly");
            Logger.getLogger(addnewdoctor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

